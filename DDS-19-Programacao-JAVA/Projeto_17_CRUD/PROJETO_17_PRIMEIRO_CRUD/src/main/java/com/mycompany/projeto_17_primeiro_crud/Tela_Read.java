    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:

            Connection conexao = null;
            PreparedStatement statement = null;

            String url = "jdbc:mysql://127.0.0.1:3306/crud";
            String usuario = "root";
            String senha = "";

            conexao = DriverManager.getConnection(url, usuario, senha);

            String sql = "SELECT nome,sobrenome,cpf,numero FROM cadastro WHERE id = ?";

            statement = conexao.prepareStatement(sql);

            statement.setString(1, jTextField1.getText());

            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                jLabel8.setText("Cadastro Encontrado");
                String nome = resultSet.getString("Nome");
                String sobrenome = resultSet.getString("Sobrenome");
                String cpf = resultSet.getString("CPF");
                String numero = resultSet.getString("Numero");

                jLabel2.setText(nome);
                jLabel9.setText(sobrenome);
                jLabel10.setText(cpf);
                jLabel11.setText(numero);
            } else {
                jLabel8.setText("Cadastro NÃ£o Encontrado");
            }

            resultSet.close();
            statement.close();
            conexao.close();
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Read.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

